package model;

public class Guide2 {
    private String field;
    private String office;

    public Guide2(String field, String office) {
        this.field = field;
        this.office = office;
    }

    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public String getOffice() {
        return office;
    }

    public void setOffice(String office) {
        this.office = office;
    }
}
